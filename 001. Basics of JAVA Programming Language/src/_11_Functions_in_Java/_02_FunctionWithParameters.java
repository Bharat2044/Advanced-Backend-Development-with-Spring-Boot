package _11_Functions_in_JAVA;

public class _02_FunctionWithParameters {

    public static void addNumbers(int num1, int num2) {

        int sum = num1 + num2;
        System.out.println(sum);
    }

    public static void main(String[] args) {

        addNumbers(5, 10);
        addNumbers(15, 25);
        addNumbers(15, 25);
    }
}
