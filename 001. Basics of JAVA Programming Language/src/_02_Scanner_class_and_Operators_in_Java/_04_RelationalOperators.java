package _02_Scanner_class_and_Operators_in_Java;

public class _04_RelationalOperators {

    public static void main(String[] args) {

        int a = 8, b = 20;

        System.out.println(a + " " + b);

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
