package _02_Scanner_class_and_Operators_in_Java;

public class _07_BitwiseOperators {

    public static void main(String[] args) {

        int a = 5, b = 7;

        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~a);
        System.out.println(10 >> 2);
        System.out.println(10 >>> 2);
        System.out.println(10 << 2);
    }
}
